const fs = require('fs');

function binaryRestore() {
    try {
        let content = fs.readFileSync('index.html', 'binary');
        
        console.log("Starting binary restoration...");

        const map = {
            // Layer 3
            '\xC3\x83\xCB\x86\xC3\x83\xCB\x9C\xC3\x82\xC2\xA4': '✖',
            '\xC3\x83\xCB\x9C\xC3\x85\xE2\x80\x9C\xC3\x82\xC2\xA7': '📧',
            '\xC3\x83\xC2\xB0\xC3\x85\xE2\x80\x9C\xC3\x82\xC2\xB8\xC3\x82\xC2\xA7\xC3\x82\xC2\xBA': '🧁',
            
            // Standard ones
            '\xC3\x83\xC3\x82\xC3\x82\xC2\xA9': 'é',
            '\xC3\x83\xC3\x82\xC3\x82\xC2\xA8': 'è',
            '\xC3\x83\xC3\x82\xC3\x82\xC2\xA0': 'à',
            '\xC3\x83\xC3\x82\xC3\x82\xC2\xAA': 'ê',
            '\xC3\x83\xC3\x82\xC3\x82\xC2\xA7': 'ç',
            '\xC3\x83\xC2\xAB': 'ë',
            
            // These are common from the screenshots
            '\xC3\x83\xC6\x92\xC3\x82\xC2\xA9': 'é',
            '\xC3\x83\xC6\x92\xC3\x82\xC2\xA8': 'è',
            '\xC3\x83\xC6\x92\xC3\x82\xC2\xA0': 'à',
            '\xC3\x83\xC6\x92\xC3\x82\xC2\xAA': 'ê',
            
            // Emojis (Patterns from dashAvatar)
            '\xC3\x83\xC2\xB0\xC3\x85\xE2\x80\x9C\xC3\x82\xC2\x9F\xC3\x82\xC2\x91\xC3\x82\xC2\xA8\xC3\x83\xC2\xA2\xC3\x82\xE2\x82\xAC\xC3\x82\xC2\x9D\xC3\x83\xC2\xB0\xC3\x85\xE2\x80\x9C\xC3\x82\xC2\x9F\xC3\x82\xC2\x8D\xC3\x82\xC2\xB3': '👨‍🍳'
        };

        // Aggressive replacement of common sequences found in the file
        content = content.replace(/\xC3\x83\xC3\x82\xC3\x82\xC2\xA9/g, 'é');
        content = content.replace(/\xC3\x83\xC6\x92\xC3\x82\xC2\xA9/g, 'é');
        content = content.replace(/\xC3\x83\xC3\x82\xC3\x82\xC2\xA8/g, 'è');
        content = content.replace(/\xC3\x83\xC6\x92\xC3\x82\xC2\xA8/g, 'è');
        content = content.replace(/\xC3\x83\xC3\x82\xC3\x82\xC2\xA0/g, 'à');
        content = content.replace(/\xC3\x83\xC6\x92\xC3\x82\xC2\xA0/g, 'à');
        
        // Symbols
        content = content.replace(/\xC3\x83\xC2\xA2\xC3\x82\xE2\x82\xAC\xC3\x82\xC2\xA2/g, '•');
        content = content.replace(/\xC3\x83\xC2\xA2\xC3\x82\xE2\x82\xAC\xC3\x82\xC2\xA6/g, '...');
        content = content.replace(/\xC3\x83\xC2\xA2\xC3\x82\xE2\x82\xAC\xC3\x82\xE2\x80\x94/g, '—');
        
        // This is the big one for emojis
        content = content.replace(/\xC3\x83\xC2\xB0\xC3\x85\xE2\x80\x9C\xC3\x82\xC2\x9F\xC3\x82\xC2\x93\xC3\x85\xC2\xA0/g, '📊');
        content = content.replace(/\xC3\x83\xC2\xB0\xC3\x85\xE2\x80\x9C\xC3\x82\xC2\x9F\xC3\x82\xC2\xA7\xC3\x82\xC2\xBA/g, '🧁');
        content = content.replace(/\xC3\x83\xC2\xB0\xC3\x85\xE2\x80\x9C\xC3\x82\xC2\x9F\xC3\x82\xC2\x93\xC3\x82\xC2\xA6/g, '📦');
        content = content.replace(/\xC3\x83\xC2\xB0\xC3\x85\xE2\x80\x9C\xC3\x82\xC2\x9F\xC3\x82\xC2\x93\xC3\x82\xC2\x88/g, '📈');

        fs.writeFileSync('index.html', content, 'binary');
        console.log("Binary restoration complete!");
    } catch (err) {
        console.error("Error:", err);
    }
}

binaryRestore();
